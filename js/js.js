// JavaScript Document

function yishi()
{
	var odiv=document.getElementById('xkzx_ys');
	
	if (odiv.style.display=='block')
	{
		odiv.style.display='none';	
	}	
	else
	{
		odiv.style.display='block';	
	}
}

